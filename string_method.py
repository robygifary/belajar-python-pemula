# Belajar string method

nama = "robby tamara gifary"
print(nama)

# merubah str menjadi huruf besar
print(nama.upper())

# merubah str huruf besar didepannya saja
print(nama.capitalize())

# merubah str huruf besar di tiap karakter
print(nama.title())

# 
print(nama.split(" "))

# mengambil karater str
print(nama[7])

# mengambil karater str dari belakang
print(nama[-1])

# mengambil sebagian karater str
print(nama[0:5])
